package com.atlassian.connector.eclipse.ui.team;

/**
 * Model for a repository
 * 
 * @author Thomas Ehrnhoefer
 */
public class CustomRepository implements Comparable<CustomRepository> {

	private final String url;

	public CustomRepository(String url) {
		super();
		this.url = url;
	}

	public String getUrl() {
		return url;
	}

	public int compareTo(CustomRepository other) {
		return this.url.compareTo(other.getUrl());
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((url == null) ? 0 : url.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj) {
			return true;
		}
		if (obj == null) {
			return false;
		}
		if (getClass() != obj.getClass()) {
			return false;
		}
		CustomRepository other = (CustomRepository) obj;
		if (url == null) {
			if (other.url != null) {
				return false;
			}
		} else if (!url.equals(other.url)) {
			return false;
		}
		return true;
	}

}
